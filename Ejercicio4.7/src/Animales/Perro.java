package Animales;

public class Perro extends Canido {

    /**
     * Metodo que devuelve un String con el sonido de un perro
     * @return Un valor String con el sonido de un perro: "Ladrido"
     */
    public String getSonido() {
        return "Ladrido";
    }

    /**
     * Metodo que devuelve un String con los alimentos de un perro
     * @return Un valor String con la alimentacion de un perro: "Carnivoro"
     */
    public String getAlimentos() {
        return "Carnivoro";
    }

    /**
     * Metodo que devuelve un String con el habitat de un perro
     * @return Un valor String con el habitat de un perro: "Domestico"
     */
    public String getHabitat() {
        return "Domestico";
    }

    /**
     * Metodo que devuelve un String con el nombre cientifico de un perro
     * @return Un valor String con el nombre cientifico de un perro:
     *         "Canis lupus familiaris"
     */
    public String getNombreCientifico() {
        return "Canis lupus familiaris";
    }
}
